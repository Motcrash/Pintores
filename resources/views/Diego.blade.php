    contenido político y social en edificios públicos. La obra de Diego y de su esposa, la pintora Frida
    Kahlo, se influyeron mutuamente. Fue creador de diversos murales en distintos puntos del ahora
    llamado Centro Histórico de la Ciudad de México, así como en la Escuela Nacional de Agricultura de
