<p>Leonardo da Vinci(Vinci, 15 de abril de
    14522​-Amboise, 2 de mayo de 1519) fue un polímata florentino del Renacimiento italiano. Fue a la
    vez pintor, anatomista, arquitecto, paleontólogo,​ artista, botánico, científico, escritor,
    escultor, filósofo, ingeniero, inventor, músico, poeta y urbanista. Murió acompañado de Francesco
    Melzi,​ a quien legó sus proyectos, diseños y pinturas. Tras pasar su infancia en su ciudad
    natal, Leonardo estudió con el pintor florentino Andrea de Verrocchio. Sus primeros trabajos de
    importancia fueron creados en Milán al servicio del duque Ludovico Sforza. Trabajó a continuación en
    Roma, Bolonia y Venecia, y pasó sus últimos años en Francia, por invitación del rey Francisco I.
